package org.unibayreuth.regextest.compilers.utils;

public class Counter {
    private int min;
    private int max;

    public Counter(int min, int max) {
        this.min = min;
        this.max = max;
    }

    public int getMin() {
        return min;
    }

    public int getMax() {
        return max;
    }
}
